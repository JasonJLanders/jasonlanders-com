/**
 * Build a flat roster of unique people across all roles.
 * Returns [{ name, role, city, region, relatedStats }]
 * TBH and UNASSIGNED SEs are excluded.
 */
export function getRoster(data, addedSEs = []) {
  const roster = [];

  // SEs
  const sesSeen = new Set();
  data.forEach(row => {
    if (!row.se || row.se.startsWith('TBH') || row.se === 'UNASSIGNED') return;
    if (sesSeen.has(row.se)) return;
    sesSeen.add(row.se);
    const rows = data.filter(r => r.se === row.se);
    roster.push({
      name: row.se, role: 'SE', city: row.home_city, region: row.ae_region,
      relatedStats: { accountCount: rows.length, aeCount: new Set(rows.map(r => r.ae)).size, segment: row.segment }
    });
  });
  addedSEs.forEach(s => {
    if (!s.se || sesSeen.has(s.se)) return;
    sesSeen.add(s.se);
    roster.push({ name: s.se, role: 'SE', city: s.home_city, region: s.ae_region,
      relatedStats: { accountCount: 0, aeCount: 0, segment: s.segment || 'Key' } });
  });

  // AEs
  const aesSeen = new Set();
  data.forEach(row => {
    if (!row.ae || aesSeen.has(row.ae)) return;
    aesSeen.add(row.ae);
    const rows = data.filter(r => r.ae === row.ae);
    roster.push({
      name: row.ae, role: 'AE', city: row.ae_city, region: row.ae_region,
      relatedStats: { accountCount: rows.length }
    });
  });

  // RDs
  const rdsSeen = new Set();
  data.forEach(row => {
    if (!row.rd || rdsSeen.has(row.rd)) return;
    rdsSeen.add(row.rd);
    const rows = data.filter(r => r.rd === row.rd);
    roster.push({
      name: row.rd, role: 'RD', city: row.rd_city, region: row.ae_region,
      relatedStats: { aeCount: new Set(rows.map(r => r.ae)).size }
    });
  });

  // RVPs
  const rvpsSeen = new Set();
  data.forEach(row => {
    if (!row.rvp || rvpsSeen.has(row.rvp)) return;
    rvpsSeen.add(row.rvp);
    const rows = data.filter(r => r.rvp === row.rvp);
    roster.push({
      name: row.rvp, role: 'RVP', city: row.rvp_city, region: row.ae_region,
      relatedStats: { rdCount: new Set(rows.map(r => r.rd)).size }
    });
  });

  // SE Leaders
  const leadersSeen = new Set();
  data.forEach(row => {
    if (!row.se_leader || leadersSeen.has(row.se_leader)) return;
    leadersSeen.add(row.se_leader);
    const rows = data.filter(r => r.se_leader === row.se_leader && !r.se.startsWith('TBH') && r.se !== 'UNASSIGNED');
    roster.push({
      name: row.se_leader, role: 'SELeader', city: row.se_leader_city, region: row.ae_region,
      relatedStats: { seCount: new Set(rows.map(r => r.se)).size }
    });
  });

  return roster;
}
